    <script>
      $(function() {
        //swiper 768以下で起動
        var swiper; 
        $(window).on('load resize', function(){
            var w = $(window).width();
            if (w <= 768) {
                if (swiper) {
                    return;
                } else {
                  var swiper = new Swiper('.swiper-container', {
                    navigation: {
                      nextEl: '.swiper-button-next',
                      prevEl: '.swiper-button-prev',
                    },
                    loop: true,
                    autoplay: {
                        delay: 2000,
                        disableOnInteraction: false,
                      },
                  });
                }
            } else {
                if (swiper) {
                    swiper.destroy();
                    swiper = undefined;
                }
            }
        });
    });
